public class DayofWeek {
    public static void main(String[] args) {
        int DayofWeekNum = 8;
        switch (DayofWeekNum) {
            case 1:
                System.out.println("MON");
                break;
            case 2:
                System.out.println("TUE");
                break;
            case 3:
                System.out.println("WEN");
                break;
            case 4:
                System.out.println("THR");

            default:
                System.out.println("You are wrong.");
                break;
        }
    }

}
